package Object_Oriented_Programming.ClassAndObject;

public class ClassAndObject {
    public static void main(String[] args) {
        // Creating an object of class Car
        Car myCar = new Car();
        
        // Setting attributes
        myCar.color = "Red";
        myCar.model = "Toyota";

        // Calling behavior
        myCar.drive();
    }
}
